</tr>
<tr>
	<td class=tdmainobg>-></td>
	<td class=tdmainobg>§2.2</td>
	<td class=tdmainobg>Angriffe gegen Accounts, die in der Siedlerliste mit einem roten Stern gekennzeichnet sind untersagt und führen zur Sperrung des Accounts</td>
</tr>
<tr>
	<td class=tdmainobg>§3</td>
	<td class=tdmainobg></td>
	<td class=tdmainobg>Jede Art von Spieler-, Schiffs-, Kolonie- und Allianznamen ist erlaubt, es sei denn</td>
</tr>
<tr>
	<td class=tdmainobg>-></td>
	<td class=tdmainobg>§3.1</td>
	<td class=tdmainobg>Der Name verstößt gegen die Netiquette</td>
</tr>
<tr>
	<td class=tdmainobg>-></td>
	<td class=tdmainobg>§3.2</td>
	<td class=tdmainobg>Der Name ist bereits in gleicher oder sehr ähnlicher weise vorhanden</td>
</tr>
<tr>
	<td class=tdmainobg>-></td>
	<td class=tdmainobg>§3.3</td>
	<td class=tdmainobg>Der Name fälscht einen NPC oder verdeckt die Spieler-ID</td>
</tr>
<tr>
	<td class=tdmainobg>-></td>
	<td class=tdmainobg>§3.4</td>
	<td class=tdmainobg>Der Name ist durch Schwärzen oder andere Mittel unkenntlich gemacht</td>
</tr>
<tr>
	<td class=tdmainobg>-></td>
	<td class=tdmainobg>§3.5</td>
	<td class=tdmainobg>Es ist untersagt, mithilfe geänderter Namen/Schiffsnamen oder anderer Mittel Mitteilungen in die History zu setzen. (Beispielsweise durch Selbstzerstörung eines Schiffes)</td>
</tr>
<tr>
	<td class=tdmainobg>§4</td>
	<td class=tdmainobg></td>
	<td class=tdmainobg>Es ist strengstens untersagt zu einem bestehenden Spielaccount einen weiteren Account zu registrieren (sei es für sich selbst oder für einen Freund/etc)</td>
</tr>
<tr>
	<td class=tdmainobg>-></td>
	<td class=tdmainobg>§4.1</td>
	<td class=tdmainobg>Es ist ebenfalls untersagt, einen Account zu übernehmen (warm zu halten) um ihn oder seine Waren etc. später weiter zu geben</td>
</tr>
<tr>
	<td class=tdmainobg>-></td>
	<td class=tdmainobg>§4.2</td>
	<td class=tdmainobg>Es ist ebenfalls untersagt, Accounts erstellen zu lassen dessen Waren zur eigenen Bereicherung dienen.</td>
</tr>
<tr>
	<td class=tdmainobg>-></td>
	<td class=tdmainobg>§4.3</td>
	<td class=tdmainobg>Übertragung der Startwaren an einen anderen Account ist untersagt.</td>
</tr>
<tr>
	<td class=tdmainobg>-></td>
	<td class=tdmainobg>§4.4</td>
	<td class=tdmainobg>Das Weitergeben von Accounts ist untersagt.</td>
</tr>
<tr>
	<td class=tdmainobg>--></td>
	<td class=tdmainobg>§4.4.1</td>
	<td class=tdmainobg>Das Zwischenlagern von Waren eines anderen Accounts die dann an einen neuen Account zurücktransferiert werden, ist untersagt</td>
</tr>
<tr>
	<td class=tdmainobg>-></td>
	<td class=tdmainobg>§4.5</td>
	<td class=tdmainobg>Accounts, die nur (oder zu einem Großteil) dazu dienen, Waren oder Energie zu speichern und als "Depot" zu dienen, sind verboten.</td>
</tr>
<tr>
	<td class=tdmainobg>§5</td>
	<td class=tdmainobg></td>
	<td class=tdmainobg>Angebote in der Warenbörse (sog. Lagerangebote), die einzig und allein den Zweck erfüllen, der Besteuerung zu entgehen, können ohne Vorwarnung gelöscht werden</td>
</tr>
<tr>
	<td class=tdmainobg>-></td>
	<td class=tdmainobg>§5.1</td>
	<td class=tdmainobg>Bei mehrfachem Verstoß ist es der betreffenden Handelsinstitution erlaubt, die Waren zum Teil oder komplett einzuziehen. Hierbei gibt es keinen Schadensersatz</td>
</tr>
<tr>
	<td class=tdmainobg>§6</td>
	<td class=tdmainobg></td>
	<td class=tdmainobg>Das Ausnützen jedes Fehlers (Bug) ist untersagt und muss sofort im Bug-Forum gemeldet werden (wenn dazu nicht schon ein Thema im Forum existiert). Sollte dies nicht der Fall sein behalten die Admins die Sperrung/Löschung des betreffenden Accounts vor</td>
</tr>
<tr>
	<td class=tdmainobg>-></td>
	<td class=tdmainobg>§6.1</td>
	<td class=tdmainobg>Das "Hacken" (*) des Spielservers oder des Forums ist strengstens untersagt. Zuwiderhandlungen werden zur Anzeige gebracht</td>
</tr>
<tr>
	<td class=tdmainobg>§7</td>
	<td class=tdmainobg></td>
	<td class=tdmainobg><strong>Das Verwenden sowie das automatisierte Auslesen von Spielinformationen in jeglicher Form ist ohne die Erlaubnis der Admins nicht gestattet (Bereitstellung und/oder Nutzung). Darunter fallen u.a. Sternenkarten, Tools für Preisberechnung für Module, etc</strong></td>
</tr>
<tr>
	<td class=tdmainobg>§8</td>
	<td class=tdmainobg></td>
	<td class=tdmainobg>Es gilt die Netiquette. Spieler, die mehrfach negativ auffallen oder den Spielbetrieb stören können vom Spiel ausgeschlossen werden</td>
</tr>
<tr>
	<td class=tdmainobg>-></td>
	<td class=tdmainobg>§8.1</td>
	<td class=tdmainobg>Das Versenden von Unmengen von Privaten Nachrichten an andere Spieler (Allianzbeitrittsangebote an alle Neusiedler oder ähnliches) ist untersagt.</td>
</tr>
<tr>
	<td class=tdmainobg>§9</td>
	<td class=tdmainobg></td>
	<td class=tdmainobg>Es wird jedem Spieler nahe gelegt, sein ingame Verhalten durch ein RPG zu begründen/stützen</td>
</tr>
<tr>
	<td class=tdmainobg>§10</td>
	<td class=tdmainobg></td>
	<td class=tdmainobg>Die Admins behalten sich vor, die Regeln jederzeit rückwirkend zu ändern. Für die "Strafverfolgung" werden private Nachrichten sowie Nutzungsstatistiken geprüft</td>
</tr>
<tr>
	<td class=tdmainobg>§11</td>
	<td class=tdmainobg></td>
	<td class=tdmainobg>Unwissenheit schützt vor Strafe nicht</td>
</tr>
<tr>
	<td class=tdmainobg>§12</td>
	<td class=tdmainobg></td>
	<td class=tdmainobg>Diese Regeln sind als Leitfaden für Spieler gedacht. Die Admins behalten sich vor, auch Vergehen, die nicht ausdrücklich den Regeln widersprechen, zu bestrafen. Aus den Regeln ergeben sich ausserdem keinerlei Ansprüche und sie können nicht zur Verteidigung vor Gericht verwendet werden</td>
</tr>
<tr>
