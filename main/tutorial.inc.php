      <br> <p>1. Schaltet zuerst die &quot;Langstrecken Sensoren&quot; ein, damit 
        ihr nicht ausversehen irgendwo reinfliegt, das euch viel Energie kostet 
        oder, am schlimmsten, euch die Crew kostet.</p>
      <p> Das rot umrahmte ist euer Steuer-Pad. Damit fliegt ihr - hoch, runter, 
        links, lrechts - jeweils ein Feld. Wollt ihr mehrere Felder auf einmal 
        Fliegen (nur für erfahrene Piloten ;) ) benutzt ihr den Autopilot rechts 
        davon.</p>
      <p>Nun solltet ihr euch zu eurem Planeten durchschlagen -und achtet darauf, 
        dass ihr auf normalen Raumfeldern bleibt (<img border="0" src="http://home.arcor.de/omega-sektion/stu/map/1.gif" width="30" height="30">).</p>
      <p>3. Solltet ihr auf eurem Weg keine Energie mehr haben könnt ihr die Ersatzbatterien 
        entladen.</p>
      <p>Solltet ihr dennoch nicht an euer Ziel kommen, habt ihr euch entweder 
        verflogen oder wirklich einen Planet am Arsch des Universums ausgesucht 
        ;).</p>
      <p>In diesem Fall müsst ihr auf die neue Runde warten, dann produziert euer 
        Schiff, vorausgesetzt ihr habt Deuterium dabei, Energie.</p>
      <p>Die Rundenwechsel sind um 12, 15, 18, 21 und 0 Uhr.</p>
