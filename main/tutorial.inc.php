      <p>2. Nun solltest du, bevor du ein Schiff von einem der 2 Handelsallianz-Posten 
        beantragst, nachschauen, wo ein naher und vor allem freier Klasse M Planet 
        ist.</p>
      <p>3. Vergleiche am besten die Planetenliste mit den Koordinaten der 2 Posten.</p>
      <p>Sollte die erste/zweite Zahl des Planeten ziemlich nah zur ersten/zweiten 
        Zahl einer der Posten sein, ist der Planet nicht weit vom Posten entfernt. 
        Das ist vor allem gut, damit man gleich beim ersten Versuch zu seinem 
        Planeten gelangt.</p>
      <p>Ihr könnt euch natürlich auch einen weiter entfernten Planeten aussuchen, 
        das ist eure Sache.</p>
